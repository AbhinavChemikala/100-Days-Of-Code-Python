# Defining Functions
def my_function():
    print("Hello")
    print("Bye!")

my_function() # Calling the function

# While Loops
number = 5
while number > 0: # While loop
    print(number) # Prints 5, 4, 3, 2, 1
    number -= 1 # number = number - 1

number = 3
# While not loop
while not number == 0: 
    print(number) # Prints 5, 4, 3, 2, 1
    number -= 1 # number = number - 1

